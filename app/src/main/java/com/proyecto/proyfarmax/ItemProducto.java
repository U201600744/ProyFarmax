package com.proyecto.proyfarmax;

public class ItemProducto {

    String item_nombreProducto, item_stock, item_precio;

    public String getItem_nombreProducto() {
        return item_nombreProducto;
    }

    public String getItem_stock() {
        return item_stock;
    }

    public String getItem_precio() {
        return item_precio;
    }

    public void setItem_nombreProducto(String item_nombreProducto) {
        this.item_nombreProducto = item_nombreProducto;
    }

    public void setItem_stock(String item_stock) {
        this.item_stock = item_stock;
    }

    public void setItem_precio(String item_precio) {
        this.item_precio = item_precio;
    }
}
